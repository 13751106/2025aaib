#include<stdio.h> ///檔名不能寫錯
/// stdio 的意思是 標準的standard input/output
/// 有了 stdio.h 的外掛, 才能用 scanf() printf()
int main()
{
	int a, b; /// 整數 a, b
	scanf( "%d" , &a); /// 正課剛剛交, 掃描讀東西
	scanf( "%d" , &b); /// 可以用複製的, 再改 b

	printf( "%d" , a+b);

}
